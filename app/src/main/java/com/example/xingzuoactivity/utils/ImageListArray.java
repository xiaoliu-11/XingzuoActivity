package com.example.xingzuoactivity.utils;

public class ImageListArray {
    private String name;
    private int imageId;

    public ImageListArray(String name, int imageId){
        this.name = name;
        this.imageId = imageId;
    }
    public String getName() {
        return name;
    }
    public int getImageId() {
        return imageId;
    }
}
